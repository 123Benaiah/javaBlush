public class MultipliTable {

    public static void main(String[] args) {
    // create a multiplication table
 int i,a,j;
 for (i=1;i<=12;i++){
    
     System.out.print(i+"\t");
    for(j=2;j<=12;j++){
        a=j*i;
    System.out.print(a+"\t");
    }
 System.out.println("\n\n\n");
   }
 
    }
    
    
}
    